package day3;

public class WholeNumber1 {
	public static void main(String args[]) {
		byte b = 13;
		int num = b;
		//                  13
		System.out.println(num);

		int num1 = 12;
		byte b2 = (byte)num1;
		System.out.println(b2);

		// for whole numbers we have
		// byte -> short -> int -> long

		short num2 = 1234;

		long l = 413434;
	}
}