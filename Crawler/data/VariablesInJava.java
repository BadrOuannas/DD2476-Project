package day2;

public class VariablesInJava {
	public static void main(String args[]) {
		String firstName;

		firstName = "John";
		// concatenation
		System.out.println("Hello " + firstName);

		firstName = "Beknazar";
		System.out.println(firstName);

		firstName = "Alex";
		System.out.println(firstName);

	}
}