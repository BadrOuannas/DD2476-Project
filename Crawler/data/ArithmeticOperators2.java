package day5;

public class ArithmeticOperators2 {
	public static void main(String[] args) {
		// * / % + -
		int result = 2 + 3 * 2; 
		int result1 = (2 + 3) * 2; 

		System.out.println(result); // 8   
		System.out.println(result1); // 10

		System.out.println("--------------");
        // 17 / 10 = 1 -> 7
		System.out.println(1010 % 10);  // 0
		System.out.println(12 % 3);    // 0
		System.out.println(19 % 10);  // 9
		

		// BREAK 10 min till 8:12 PM

	}
}