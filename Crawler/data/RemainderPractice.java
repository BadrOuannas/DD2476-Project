package day4;

public class RemainderPractice {
	public static int num;

	public static void main(String[] args) {

		num = 3;
		System.out.println(7 % num);   // 1
		System.out.println(19 % num);  // 1
		System.out.println(12 % num);  // 0
		System.out.println(99 % num);  // 0
		System.out.println(47 % num);  // 2
	}
}