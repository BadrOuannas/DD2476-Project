package day5;

public class ArithmeticOperators1 {
	public static void main(String[] args) {
		int number = 15;
		int result = number / 2;
		System.out.println(result); // 7

		double dNumber = 48.0;
		double dResult = dNumber / 7;
		System.out.println(dResult); // 6.8
	} 
}