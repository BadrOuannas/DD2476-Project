package com.helloworld.javapractice;

/*
类名要求：
类名必须以英文字母开头，后接字母，数字和下划线的组合
习惯以大写字母开头
要注意遵守命名习惯，好的类命名：
	Hello
	NoteBook
	VRPlayer
不好的类命名：
	hello
	Good123
	Note_Book
	_World
*/
public class HelloWorld {
	/*
	 方法名要求
	好的方法命名：
		main
		goodMorning
		playVR
	不好的方法命名：
		Main
		good123
		good_morning
		_playVR
	 */
	public static void main(String[] args) {
		System.out.println("hello world!");
	}
}
