package day3;

public class CharPractice {
	public static void main(String args[]) {
		char ch = 'H';
		char ch1 = 'I';

		System.out.println(ch);
		System.out.println(ch1);

		System.out.println(ch + 0);
		System.out.println(ch1 + 0);
		System.out.println("Chars: " + ch + ch1);
	}
}